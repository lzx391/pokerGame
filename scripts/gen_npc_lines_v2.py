def validate():
    counts = []
    for style, moods in CONTENT.items():
        for mood, keys in moods.items():
            for key, lines in keys.items():
                if len(lines) < 10:
                    raise ValueError(f"{style}.{mood}.{key} has {len(lines)} lines")
                for line in lines:
                    if BANNED.search(line):
                        raise ValueError(f"BANNED in {style}.{mood}.{key}: {line}")
                counts.append((style, mood, key, len(lines)))
    return counts


def _yaml_quote(s: str) -> str:
    if '"' in s or ":" in s or s.startswith(("-", "?", "!")):
        return "'" + s.replace("'", "''") + "'"
    return f'"{s}"'


def write_yaml():
    for style, meta in META.items():
        path = OUT_DIR / f"{style}.yaml"
        lines_out: list[str] = [
            f"# {style} — npc-lines v2",
            "enabled: true",
            f"speakProbability: {meta['speakProbability']}",
            f"settleSpeakProbability: {meta['settleSpeakProbability']}",
            "",
        ]
        mood_comments = {
            "happy": "# ========== 高兴（mood >= highThreshold）==========",
            "calm": "# ========== 平静（两阈值之间）==========",
            "sad": "# ========== 低落（mood <= lowThreshold）==========",
        }
        for mood in MOODS:
            lines_out.append(mood_comments[mood])
            lines_out.append(f"{mood}:")
            for key in KEYS:
                lines_out.append(f"  {key}:")
                for line in CONTENT[style][mood][key]:
                    lines_out.append(f"    - {_yaml_quote(line)}")
            lines_out.append("")
        path.write_text("\n".join(lines_out).rstrip() + "\n", encoding="utf-8")


if __name__ == "__main__":
    counts = validate()
    write_yaml()
    print(f"Wrote {len(META)} files, {len(counts)} pools validated")
